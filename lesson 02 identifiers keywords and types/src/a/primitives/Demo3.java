package a.primitives;

public class Demo3 {

	public static void main(String[] args) {
		
		System.out.println(2147483647); // literal of type int
		System.out.println(2147483648L);  // literal of type long
		
		System.out.println(5D); // literal of type double
		System.out.println(5F); // literal of type float
		
		System.out.println(0.33333333333333333333333333333333333333333333333333);  // literal of type double
		System.out.println(0.33333333333333333333333333333333333333333333333333F);  // literal of type float
		
		
	}

}
