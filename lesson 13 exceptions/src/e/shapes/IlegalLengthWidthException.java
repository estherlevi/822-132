package e.shapes;

public class IlegalLengthWidthException extends Exception {

	public IlegalLengthWidthException() {
		super();
	}

	public IlegalLengthWidthException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public IlegalLengthWidthException(String message, Throwable cause) {
		super(message, cause);
	}

	public IlegalLengthWidthException(String message) {
		super(message);
	}

	public IlegalLengthWidthException(Throwable cause) {
		super(cause);
	}

}
